public class NamingConvention 
{
public static void main(String[] args) 
{
// Variable
int myAge = 19;

// Method
myMethod();

// Constants
final int MAX_AGE = 60;
System.out.println("1.Variable: "+myAge+"\n2.Constant: "+MAX_AGE+"");
}

// Method
static void myMethod() {
    System.out.println("This is the method's value");
} 
}
